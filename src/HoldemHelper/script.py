import numpy as np
def add_three(number):
    """
    Adds 3 to your number.

    Args:
        number: Your chosen number.

    Returns:
        The sum your number and 3.
    """
    return np.add(number, 3)


def add_five(num):
    """
    Adds 5 to your number.

    Args: 
        num: Your chosen number.

    Returns:
        The sum of your number and 5.
    """
    return num + 5